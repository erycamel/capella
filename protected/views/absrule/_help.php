<h1 class="h1help">Pendahuluan</h1>
<p class="phelp">
Modul Absence Rule digunakan untuk mendaftarkan detail aturan main absensi dari daftar schedule.<br/>
Misalkan: Shift 1, Jam masuk 05:00, Jam pulang 13:00, maka absence rule-nya menjadi : <br/>
Shift 1, Time In 05:00, Time Out 06:59, Absence status M <br/>
dan begitu seterusnya hingga bertemu dengan jam pulang <br/>
Shift 1, Time In 15:00, Time Out 23:00, Absence status<br/>
</p>
<h1 class="h1help">Relasi Modul</h1>
<p>
Data yang dimasukkan di modul Absence Rule digunakan di modul : Transaksi Absensi  
</p>
