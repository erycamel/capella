<h1>Pendahuluan</h1>
Modul ini digunakan untuk memasukkan nama perusahaan / orang pribadi yang bersifat<br/>
external yang berhubungan dengan perusahaan.<br/>
Hubungan ini dapat bersifat karyawan, supplier, customer, bank, dan lain-lain <br/>
Setiap perusahaan dapat berupa hubungan lebih dari satu jenis, misalkan <br/>
perusahaan A sebagai supplier sekaligus customer<br/>